#include "header.h"

void	clear_2dbuffer(int h, int w, int buf[h][w])
{
	int i;
	int j;

	i = 0;
	while (i < h)
	{
		j = 0;
		while (j < w)
		{
			buf[i][j] = 0;
			j++;
		}
		i++;
	}
}
